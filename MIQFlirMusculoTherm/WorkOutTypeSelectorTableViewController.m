#import "WorkOutTypeSelectorTableViewController.h"
#import "DemoSelectorViewController.h"

@interface WorkOutTypeSelectorTableViewController ()

@property (strong, nonatomic)NSArray * workoutTypeArr;

@end

static NSString * const cellIdentifier = @"cell";

@implementation WorkOutTypeSelectorTableViewController

- (void)viewDidLoad {
    [super viewDidLoad];
    self.workoutTypeArr = @[@"Back of Legs", @"Front of Legs", @"Arms", @"Chest" ,@"Back"];
    [self.tableView registerClass:[UITableViewCell class] forCellReuseIdentifier:cellIdentifier];
    UIImageView *imageView = [[UIImageView alloc] initWithImage:[UIImage imageNamed:@"logo_nav"]];
    self.navigationItem.titleView = imageView;
}

- (void)didReceiveMemoryWarning {
    [super didReceiveMemoryWarning];
    // Dispose of any resources that can be recreated.
}

#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return self.workoutTypeArr.count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:cellIdentifier forIndexPath:indexPath];
    cell.textLabel.text = self.workoutTypeArr[indexPath.row];
    return cell;
}


#pragma mark - Table view delegate

// In a xib-based application, navigation from a table can be handled in -tableView:didSelectRowAtIndexPath:
- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    DemoSelectorViewController *demoSelectorVC = [[DemoSelectorViewController alloc] init];
    demoSelectorVC.workoutType = indexPath.row;
    [self.navigationController pushViewController:demoSelectorVC animated:YES];
}

